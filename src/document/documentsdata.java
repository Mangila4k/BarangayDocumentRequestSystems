    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
     Session ses = Session.getInstance();

    if (ses.getId() == 0) {
        JOptionPane.showMessageDialog(null, "You must log in first.");
        Loginform lf = new Loginform();
        lf.setVisible(true);
        this.dispose();
    } else {
        dbConnector dbc = new dbConnector();
        try {
            ResultSet rs = dbc.getData("SELECT COUNT(*) AS total FROM tbl_documents");
            if (rs.next()) {
                int count = rs.getInt("total");
                numofdocs.setText("Documents: "+ count);
            }
            rs.close();
        } catch (Exception ex) {
            System.out.println("Error retrieving document count: " + ex.getMessage());
        }
    }
    }//GEN-LAST:event_formWindowActivated

