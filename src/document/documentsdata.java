    private void editdocsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editdocsMouseClicked
        if (selectedId.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please select a document to edit.");
        return;
    }

    dbConnector dbc = new dbConnector();
    try {
        String query = "SELECT * FROM tbl_documents WHERE d_id = '" + selectedId + "'";
        ResultSet rs = dbc.getData(query);

        if (rs.next()) {
            createnewdocs editForm = new createnewdocs();

            // Set fields in the edit form
            editForm.did.setText(rs.getString("d_id"));
            editForm.dtype.setText(rs.getString("d_type"));
            editForm.dprice.setText(rs.getString("d_price"));

            // Disable ID editing so that the ID can't be changed
            editForm.did.setEnabled(false);

            // Show the edit form
            editForm.setVisible(true);
            this.dispose(); // Close the current form after opening the edit form
        }

        rs.close();
    } catch (Exception ex) {
        JOptionPane.showMessageDialog(null, "Error loading document data: " + ex.getMessage());
    }
    }//GEN-LAST:event_editdocsMouseClicked

    private void deldocsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deldocsMouseClicked
        if (selectedId.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please select a document to delete.");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this document?", "Confirm Delete", JOptionPane.YES_NO_OPTION);

    if (confirm == JOptionPane.YES_OPTION) {
        dbConnector dbc = new dbConnector();
        String deleteQuery = "DELETE FROM tbl_documents WHERE d_id = '" + selectedId + "'";
        boolean result = dbc.updateData(deleteQuery);

        if (result) {
            JOptionPane.showMessageDialog(null, "Document deleted successfully.");
            displayData();
            selectedId = "";
        } else {
            JOptionPane.showMessageDialog(null, "Failed to delete document.");
        }
    }
    }//GEN-LAST:event_deldocsMouseClicked

