    private void formWindowActivated(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowActivated
        Session ses = Session.getInstance();
        if(ses.getId() == 0){
            JOptionPane.showMessageDialog(null, "No Account, Login First!");
            Loginform lf = new Loginform();
            lf.setVisible(true);
            this.dispose();
        }else{
        adname.setText(ses.getFirst_name() + " " + ses.getLast_name());
        }
    }//GEN-LAST:event_formWindowActivated

    private void dcnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_dcnMouseClicked
    for (javax.swing.JInternalFrame frame : jDesktopPane1.getAllFrames()) {
        if (frame instanceof documentdata) {
            frame.toFront();
            frame.setVisible(true);
            return;
        }
    }

    documentdata dd = new documentdata();
    jDesktopPane1.add(dd);
    dd.setVisible(true);
    }//GEN-LAST:event_dcnMouseClicked

