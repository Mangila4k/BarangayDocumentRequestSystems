    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
    int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to log out?", "Logout", JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            Loginform login = new Loginform();  // Open login form
            login.setVisible(true);
            this.dispose();  // Close current window (logout)
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void rqnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_rqnMouseClicked
    for (javax.swing.JInternalFrame frame : jDesktopPane1.getAllFrames()) {
    if (frame instanceof reqdocforms) {
        frame.toFront();
        frame.setVisible(true);
        try {
            frame.setSelected(true);
        } catch (java.beans.PropertyVetoException ex) {
            ex.printStackTrace();
        }
        return; // already open, no need to add another
    }
}

// If not open, create and add:
reqdocforms rf = new reqdocforms();
jDesktopPane1.add(rf);
rf.setVisible(true);
try {
    rf.setSelected(true);
} catch (java.beans.PropertyVetoException ex) {
    ex.printStackTrace();
}
    }//GEN-LAST:event_rqnMouseClicked

